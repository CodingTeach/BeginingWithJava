package Classes.OneDotOne;

public class WhatAreInts {
    public static void main(String[] args){
        //ints are whole numbers
        int number1 = 23;
        int number2 = 21;
        int number3 = 93;
        int number4 = 9;
        int number5 = 42;
        System.out.println(number1);

        //You can add 2 ints together
        int number6 = number1+number2;
        System.out.println(number6);

        //You can make a int equal to it self plus another
        number6 = number6+number3;
        System.out.println(number6);

        //You can even make a int change all together;
        number6 = number5+number4;
        System.out.println(number6);

        //Other operations are able to done +,-,* and /
        //Just use the operation
    }
}
